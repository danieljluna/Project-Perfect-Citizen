#pragma once

#include <SFML/Graphics.hpp>

#include "../Engine/renderComponent.h"

///////////////////////////////////////////////////////////////////////
///@brief A render component for rendering the background of a Window.
///@deatils Do not use this component manually. This component created
/// and added to a Window in ppc::Desktop::addBackgroundCmpnt.
///@author Nader Sleem
///////////////////////////////////////////////////////////////////////
class WindowBkgndRenderCmpnt: public ppc::RenderComponent {
	
private:

///////////////////////////////////////////////////////////////////////
///@brief The sprite of the background. Must be created before passing
/// to the Ctor.
///////////////////////////////////////////////////////////////////////
	sf::Sprite* sprt;

public:

	WindowBkgndRenderCmpnt() = delete;
	
///////////////////////////////////////////////////////////////////////
///@brief Ctor
///@param [in] s The sprite that is to be the background of the Window.
///////////////////////////////////////////////////////////////////////
	WindowBkgndRenderCmpnt(sf::Sprite& s);

///////////////////////////////////////////////////////////////////////
///@brief Dtor
///////////////////////////////////////////////////////////////////////
	virtual ~WindowBkgndRenderCmpnt();

///////////////////////////////////////////////////////////////////////
///@brief The overriden virtual function from updateComponent
///////////////////////////////////////////////////////////////////////
	virtual void draw(sf::RenderTarget& target, 
		sf::RenderStates states) const override;

///////////////////////////////////////////////////////////////////////
///@brief Overriden virtual function from Component
///////////////////////////////////////////////////////////////////////
	virtual void recieveMessage(msgType code) override;
};

