#ifndef HELIOS_OBJECT_COMPONENT_H
#define HELIOS_OBJECT_COMPONENT_H

namespace hls {
class Object;


///////////////////////////////////////////////////////////////////////
/// @brief Object Component Interface Class
///////////////////////////////////////////////////////////////////////
class ObjectComponent {
public:
    ///////////////////////////////////////////////////////////////////
    /// @brief  Default Constructor
    /// @details Creates an Object Component used by the given Object,
    /// @param  objRef  Pointer to the object this component is part 
    ///                 of.
    ///////////////////////////////////////////////////////////////////
    ObjectComponent(Object* objRef = nullptr);

    ///////////////////////////////////////////////////////////////////
    /// @brief Virtual Destructor
    ///////////////////////////////////////////////////////////////////
    virtual ~ObjectComponent() = default;

    ///////////////////////////////////////////////////////////////////
    /// @brief Message Handler
    /// @details
    /// @param message Field used to recieve the message.
    ///////////////////////////////////////////////////////////////////
    virtual void recieve(Object::msgType message) = 0;
    virtual void update();
};

///////////////////////////////////////////////////////////////////////
/// @brief Interface for Input Components, which are responsible for
///     reacting to user input.
///////////////////////////////////////////////////////////////////////
class InputComponent : public ObjectComponent {

};

///////////////////////////////////////////////////////////////////////
/// @brief Interface for Dynamic Components, which are responsible for
///     handling object interactions with the world given the input.
///////////////////////////////////////////////////////////////////////
class DynamicComponent : public ObjectComponent {

};

///////////////////////////////////////////////////////////////////////
/// @brief Interface for Render Components, which are responsible for 
///     handling object representation.
///////////////////////////////////////////////////////////////////////
class RenderComponent : public ObjectComponent {

};


};   //End namespace hls


#endif  //End HELIOS_OBJECT_COMPONENT_H
