#ifndef COMPONENT_H
#define COMPONENT_H


namespace ppc {


///////////////////////////////////////////////////////////////////////
/// @brief Parent Class of all Component types.
/// @details Due to a lack of common behavior between Component sub-
///     classes, this class mostly serves as a means to create a 
///     pointer to any such sub-class.
///////////////////////////////////////////////////////////////////////
class Component {
public:

  /////////////////////////////////////////////////////////////////////
  // Destructor
  /////////////////////////////////////////////////////////////////////

    //Virtual Destructor
    virtual ~Component() {};


private:

  /////////////////////////////////////////////////////////////////////
  // Private Constructors to prevent instantiation
  /////////////////////////////////////////////////////////////////////

    //Default Constructor
    Component();


};


};      //End namespace ppc

#endif  //End COMPONENT_H
